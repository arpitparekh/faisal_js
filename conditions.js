// javascript conditions
// if else else if switch case default

let n = 34;
if (n % 2 == 0) {
  console.log('even');
} else {
  console.log('odd');
}

let value = 25;

switch (value) {
  case 1:
    console.log('one');
    break;
  case 2:
    console.log('two');
    break;
  case 3:
    console.log('three');
    break;
  case 23:
    console.log('twenty three');
    break;
  default:
    console.log('not in the list');
    break;
}
